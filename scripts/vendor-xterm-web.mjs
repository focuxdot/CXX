#!/usr/bin/env node
// Vendor @xterm/xterm（浏览器 UMD 构建）+ fit/unicode11 addon 到 web/vendor/xterm/。
//
// Terminal Mode 的网页终端渲染（internal/TERMINAL-MODE.md §14.1）：固定版本本地
// vendor 静态文件，<script src> 引入；不内联进 index.html（会近乎翻倍）、不引入
// 构建步骤、不走 CDN。主版本必须与 daemon 侧 vendor（xterm-headless.cjs）一致——
// serialize 产物要喂给这里的 xterm 恢复。升级两边一起动、一起验证。
import { execFileSync } from "node:child_process";
import { copyFileSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const XTERM_VERSION = "5.5.0";
const FIT_VERSION = "0.10.0";
const UNICODE11_VERSION = "0.8.0";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const workDir = join(root, "dist", ".xterm-web-vendor");
const outDir = join(root, "web", "vendor", "xterm");

function run(cmd, args, opts = {}) {
  console.log(`$ ${cmd} ${args.join(" ")}`);
  return execFileSync(cmd, args, { stdio: "inherit", ...opts });
}

rmSync(workDir, { recursive: true, force: true });
mkdirSync(workDir, { recursive: true });
writeFileSync(join(workDir, "package.json"), JSON.stringify({ name: "xterm-web-vendor", private: true }));
run("npm", [
  "install",
  `@xterm/xterm@${XTERM_VERSION}`,
  `@xterm/addon-fit@${FIT_VERSION}`,
  `@xterm/addon-unicode11@${UNICODE11_VERSION}`,
  "--no-audit",
  "--no-fund",
], { cwd: workDir });

mkdirSync(outDir, { recursive: true });
const nm = join(workDir, "node_modules", "@xterm");
const files = [
  [join(nm, "xterm", "lib", "xterm.js"), join(outDir, "xterm.js")],
  [join(nm, "xterm", "css", "xterm.css"), join(outDir, "xterm.css")],
  [join(nm, "addon-fit", "lib", "addon-fit.js"), join(outDir, "addon-fit.js")],
  [join(nm, "addon-unicode11", "lib", "addon-unicode11.js"), join(outDir, "addon-unicode11.js")],
];
for (const [src, dst] of files) {
  copyFileSync(src, dst);
  console.log(`  → ${dst}`);
}
writeFileSync(
  join(outDir, "VERSIONS.txt"),
  `Generated by scripts/vendor-xterm-web.mjs — do not edit by hand.\n@xterm/xterm ${XTERM_VERSION}\n@xterm/addon-fit ${FIT_VERSION}\n@xterm/addon-unicode11 ${UNICODE11_VERSION}\nMIT licensed (https://github.com/xtermjs/xterm.js)\n`,
);
console.log(`\n✓ vendored xterm ${XTERM_VERSION} web assets -> ${outDir}`);
